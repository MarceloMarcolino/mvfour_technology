    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String usuario = usuarioTextField.getText();
        String senha = new String(senhaPasswordField.getPassword());
        
        if (usuario.equals("")){
            JOptionPane.showMessageDialog(null, "Por favor, insira o nome do usu치rio.");
        }
        else if (senha.equals("")){
            JOptionPane.showMessageDialog(null, "Por favor, insira a senha.");
        }
        else if(usuario.equals("admin") && senha.equals("admin")){
            JOptionPane.showMessageDialog(null, "Bem vindo, " + usuario + "!");
        }
        else{
            JOptionPane.showMessageDialog(null, "Usu치rio ou senha inv치lido!","Aviso",JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_loginButtonActionPerformed

